public class MathOperationsDemo {

    public static void main(String args[]){

        int a = 10, b = 4;

        int c = a + b;
        int d = a - b;
        double e = a / b;
        int f = a * b;

        System.out.println("Сложение" + " - " + c);
        System.out.println("Вычитание" + " - " + d);
        System.out.println("Деление" + " - " + e);
        System.out.println("Умножение" + " - " + f);
    }

}

